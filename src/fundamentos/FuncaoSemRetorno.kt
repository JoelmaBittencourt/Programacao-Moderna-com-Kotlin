package fundamentos

fun impprimirSoma(i: Int, i1: Int) {
    println(i + i1)
}

fun imprimirDivisao(a: Int, b: Int) {
    println(a / b)
}

fun imprimirSubtracao(c: Int, d: Int) {
    println(c - d)
}

fun main(args: Array<String>) {
    impprimirSoma(4, 5)
    imprimirDivisao(7, 8)
    imprimirSubtracao(10, 7)
}


